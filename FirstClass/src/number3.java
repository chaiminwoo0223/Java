public class number3 {
    public static void main(String[] args) {
        char a='g';
        String name;
        name= "";
        switch(a) {
            case 'g':
                name+='g';
            case 'd':
                name+='d';
            case 's':
                name+='s';
            case 'c':
                name+='c';
                break;
        }
        System.out.print(name);
    }
}
